package Pojo;

public class Tarjeta {
    String nombre;
    String edad;
    String banco;
    String tarjeta;
    String total;
    String numPedido;
    public Tarjeta(String nombre, String edad,String banco,String tarjeta,String total,String numPedido) {
        this.nombre = nombre;
        this.edad = edad;
        this.banco = banco;
        this.tarjeta = tarjeta;
        this.total = total;
        this.numPedido = numPedido;

    }

}
